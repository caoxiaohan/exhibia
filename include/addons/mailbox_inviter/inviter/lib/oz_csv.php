class OzCsvReader {

	var $pos = 0;
	var $csv;
	var $n;
	var $delim;

	function OzCsvReader ($csv,$delim=',') {
		$this->csv = $csv;
		$this->n = strlen($csv);
		$this->delim = $delim;
	}

	//Returns array of columns, or null if no more records available
	function nextRow() {
		$cells = array();
		$addCount = 0;
		$n = strlen($this->csv);
		$i = $this->pos;
		$lastChar = '';
		while (true) {
			$sb = '';
			$inQuote = false;
			$eol = false;
			$quoteAllowed = true;
			$hasData = false;
			while (true) {
				if ($i>=$n) {
					//If last character read was a delimiter, then this last item is actually a blank string
					if ($lastChar==$this->delim) $hasData=true;
					$eol = true;
					break;
				}
				$c = $this->csv[$i++];
				//xx // Since we also break the line upon hitting \r, we may encounter \n on the next start of line which we would need to ignore.
				//xx if (!$hasData && $c=="\n") continue;
				
				// If the line begins with a \n character, it may be a leftover of the pervious \r of the \r\n combo. Ignore it.
				if ($c==="\n" && $lastChar==='') continue;
				
				$hasData = true;
				if ($lastChar === '"' && $c !== '"' && $inQuote) {
					$inQuote = false;
				}
				if ($c === $this->delim) {
					if ($inQuote) {
						if ($lastChar === '"') break;
						else $sb.=$c;
					} else {
						$lastChar = $c;
						break;
					}
				} else if ($c === '"') {
					if ($inQuote) {
						if ($lastChar === '"') {
							$sb.=$c;
							$c = '';
						}
					} else {
						if ($quoteAllowed) {
							$inQuote = true;
							$c = '';
						} else {
							$sb.=$c;
						}
					}
				} else if ($c === "\r") {
					//if ($inQuote) $sb.=$c;
					if ($inQuote) {
						$sb.=$c;
					} else {
						$eol = true;
						break;
						//continue;
					}
				} else if ($c === "\n") {
					if ($inQuote) {
						$sb.=$c;
					} else {
						$eol = true;
						break;
					}
				} else {
					$sb.=$c;
					$quoteAllowed = false;
				}
				$lastChar = $c;
			}
			$this->pos = $i;
			if (!$hasData) return null;
			$cells[] = $sb;
			if ($eol) return $cells;
		}
	}
}




