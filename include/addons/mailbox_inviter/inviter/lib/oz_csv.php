function oz_csv_encode ($str, $delimiter=',', $forceEscape=false) {
	$needEncoding = $forceEscape;
	$n = strlen($str);
	if (!$needEncoding) {
		for ($i=0; $i<$n; $i++) {
		 	$c = $str[$i];
			if ($c == ' ' || $c == '"' || $c == $delimiter || $c == '\r' || $c == '\n' || $c == '\t') {
				$needEncoding = true;
				break;
			}
		}
	}
	if ($needEncoding) {
		$sb='"';
		for ($i=0; $i<$n; $i++) {
		 	$c = $str[$i];
			if ($c == '"') $sb.='"';
			$sb.=$c;
		}
		$sb.= '"';
		return $sb;
	}
	else {
		return $str;
	}
}

