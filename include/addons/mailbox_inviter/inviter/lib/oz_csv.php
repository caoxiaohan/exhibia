		$MAX_REALWORDS = 8;
		
		//Remove dashes (e-mail becomes email))
		$str = str_replace('-','',$str);

		//Split to words by these characters
		$words = preg_split("/[\\s_\\-\\.\\(\\)]+/",$str);
		$realwords = array();
		foreach ($words as $word) {
			$word = strtolower(trim($word));
			if (strlen($word)>0) {
				//Limit number of parts
				if (count($realwords)>=$MAX_REALWORDS) break;
				$realwords[]=$word;
			}
		}
		return $realwords;
	}

	function _countMatches($words1,$words2) {
		$c = 0;
		$n = count($words1);
		$m = count($words2);
		for ($i = 0; $i < $n; $i++) {
			for ($j = 0; $j < $m; $j++) {
				if ($words1[$i]==$words2[$j])
					$c++;
			}
		}
		return $c;
	}

	function detectFields ($fieldHeaders, $looseMatching) {
		global $_INVITER_FIELDMAPS;
		global $_INVITER_LFIELDMAPS;

		$n = count($fieldHeaders);
		$ids = array();
		for ($i=0; $i<$n; $i++) {
		 	$ids[$i] = NULL;
			$header = trim(strtolower($fieldHeaders[$i]));
			if (isset($_INVITER_FIELDMAPS[$header])) {
				$ids[$i] = $_INVITER_FIELDMAPS[$header];
			}
			else if ($looseMatching) {
				$headerWords = $this->_toWords($header);
				$bestRank = 0;
				foreach ($_INVITER_LFIELDMAPS as $fw) {
					$fieldId = $fw[0];
					$count = $this->_countMatches($headerWords,$fw[1]);
					if ($count>0) {
						$rank = $count*65536 + (65536-1-count($fw[1]));
						if ($rank>$bestRank) {
							$bestRank = $rank;
							$ids[$i] = $fieldId;
						}
					}
				}
			}
		}
		return $ids;
	}

